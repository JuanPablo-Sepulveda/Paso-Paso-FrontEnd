// dentro de es6 podemos observar distintas funciones como 
//* Declaración de variables con let y const.
//* Funciones flecha, que ofrecen una sintaxis más concisa.
//* Template literals, que permiten crear cadenas de texto de forma más legible e interpolación de variables.
//* Desestructuración, que permite extraer valores de arrays u objetos de manera más sencilla.
//* Spread operator (...), que permite expandir elementos de un iterable.
//* Clases, que facilitan la creación de objetos y la implementación de la orientación a objetos.

// en primer lugar vemos:

// let , const y var--->ES6 introdujo dos nuevas palabras clave para declarar variables: "let" y "const". "let" permite la declaración de variables con alcance de bloque, mientras que "const" se utiliza para declarar constantes cuyos valores no pueden cambiar una vez asignados.

// Enj segundo lugar vemos:

// Arrow Functions--->Las arrow functions proporcionan una sintaxis más concisa para escribir funciones en JavaScript. En lugar de la palabra clave function, se utilizan flechas (=>) para definir una función. Además, las arrow functions no tienen su propio this, sino que heredan el this del contexto que las rodea.

//OBJETOS LITERALES 

//son una sintaxis más concisa y poderosa para crear objetos en JavaScript.Antes de ES6, la forma tradicional de crear un objeto en JavaScript era mediante el uso del constructor Object o utilizando la notación de objetos con llaves {}. Sin embargo, ES6 introdujo una sintaxis más intuitiva y expresiva para definir objetos.La sintaxis de los objetos literales ES6 permite definir directamente propiedades y métodos dentro de las llaves {} sin la necesidad de utilizar la palabra clave function para los métodos.

//CLASES
//proporciona una forma más fácil y orientada a objetos de crear objetos y definir su comportamiento. Las clases permiten crear constructoras, métodos y herencia de una manera más clara y estructurada.

//TEMPLATE LITERALS
//Con las template strings, puedes crear cadenas de texto que contienen expresiones embebidas. Estas cadenas se definen utilizando comillas invertidas (```) en lugar de comillas simples o dobles. Además, puedes insertar variables o expresiones dentro de la cadena utilizando la sintaxis ${variable}.

//DESESTRUCTURACION
//La desestructuración permite asignar valores de un objeto o arreglo a variables individuales de forma más concisa. Por ejemplo, puedes extraer propiedades de un objeto y asignarlas a variables con el mismo nombre en una sola línea de código.